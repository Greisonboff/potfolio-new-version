createDivUl = (e) => {
    elementDivUl = document.createElement('div');
    elementDivUl.setAttribute('class', e.replace(' ', '_'));
    elementDivUl.innerHTML = `<h3>${e}</h3>
                                <ul data-category= ${e.replace(' ', '_')}></ul>
                            `
    document.querySelector('body .certificates').append(elementDivUl);
}

createLi = (categoria, link, nome) => {
    elemetLi = document.createElement('li');
    elemetLi.innerHTML = `<a href=${link} data-category = ${categoria.replace(' ', '_')} target="_blank">${nome}</a>`;
    document.querySelector(`body .certificates div.${categoria.replace(' ', '_')} ul`).append(elemetLi);
}

var returnElement = (e) => {
    for (a of e) {
        if (document.querySelectorAll(`body div.${a.categoria.replace(' ', '_')}`).length === 0) {
            createDivUl(a.categoria);
        }
        createLi(a.categoria, a.link, a.nome_curso);
    }
}
